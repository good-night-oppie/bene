"""OppieBugTriageBenchmark — calibrated L3 triage-decision benchmark.

Wraps the 152-row JSONL corpus generated by the Synthetic Reality Architect
calibration pipeline (ai/scratch/2026-05-13/ofs124960_synthetic_dataset.py)
as a bene Benchmark. The corpus is built from 53 real production L3 eval
rows around the OFS-124960 sibling-confusion cluster plus 99 synthetic rows
sampled from fitted distributions.

The eighth objective `modal_collapse_resistance` directly attacks the
production failure mode: L3 wrong-routes 19 of 53 cluster cases to
OFS-124960 with a mean confidence of 0.929 (MCD = Modal Collapse Defender
target). Folding this into accuracy would hide the signal we built the
benchmark to expose, so it stays elevated.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from bene.metaharness.benchmarks.base import Benchmark, Problem

from bene.benchmarks.bug_triage.seeds import ALL_SEEDS


DATA_DIR = Path(__file__).resolve().parent / "data"
SEARCH_SET_PATH = DATA_DIR / "search_set.jsonl"
TEST_SET_PATH = DATA_DIR / "test_set.jsonl"
ADVERSARIAL_PATH = DATA_DIR / "adversarial.jsonl"
WORLD_PHYSICS_PATH = DATA_DIR / "world_physics.json"
WORLD_PHYSICS_LOCK_PATH = DATA_DIR / "world_physics.lock.json"

MODAL_MAGNET_OFS = "OFS-124960"
# Multi-magnet set for v2 anchors. Scoring penalises wrong-route to ANY of
# these. Keep MODAL_MAGNET_OFS as legacy alias for back-compat.
MODAL_MAGNETS = frozenset(
    {
        "OFS-124960",  # v1 sibling-confusion cluster
        "OFS-118883",  # v2 upgrade systest umbrella
        "OFS-130800",  # v2 fs/portal/log_repl chaos umbrella
        "OFS-130479",  # v2 azure quorum drain umbrella
        "OFS-131474",  # v2 hw_qcore reimage magnet
    }
)


def _load_problems(path: Path) -> list[Problem]:
    if not path.exists():
        return []
    problems: list[Problem] = []
    with path.open(encoding="utf-8") as fh:
        for line in fh:
            line = line.strip()
            if not line:
                continue
            row = json.loads(line)
            problems.append(
                Problem(
                    problem_id=row["id"],
                    input=row["input"],
                    expected=row["expected"],
                    metadata={"provenance": row.get("provenance")},
                )
            )
    return problems


class OppieBugTriageBenchmark(Benchmark):
    """Calibrated synthetic + production L3 triage decisions, 8-objective Pareto.

    Objectives:
      +accuracy                     — mean of action/duplicate/fix-incomplete/lineage exact matches
      +pre_fix_duplicate            — accuracy on pre-fix-build subset
      +post_fix_no_duplicate        — never duplicate to fixed ticket on post-fix builds
      +fix_incomplete_precision     — P(true fix_incomplete | predicted fix_incomplete)
      +new_bug_precision            — P(true new_bug | predicted new_bug)
      +modal_collapse_resistance    — 1.0 - rate(wrong-route to OFS-124960 modal magnet)
      +evidence_quality             — required_evidence substrings present in output
      -context_cost                 — token usage proxy
    """

    @property
    def name(self) -> str:
        return "bug_triage"

    @property
    def objectives(self) -> list[str]:
        return [
            "+accuracy",
            "+pre_fix_duplicate",
            "+post_fix_no_duplicate",
            "+fix_incomplete_precision",
            "+new_bug_precision",
            "+modal_collapse_resistance",
            "+evidence_quality",
            "-context_cost",
        ]

    def get_search_set(self) -> list[Problem]:
        return _load_problems(SEARCH_SET_PATH)

    def get_test_set(self) -> list[Problem]:
        return _load_problems(TEST_SET_PATH)

    def get_adversarial_set(self) -> list[Problem]:
        return _load_problems(ADVERSARIAL_PATH)

    def get_seed_harnesses(self) -> list[str]:
        return list(ALL_SEEDS)

    def score(self, problem: Problem, output: dict[str, Any]) -> dict[str, float]:
        expected = problem.expected
        action_ok = output.get("action") == expected.get("action")
        duplicate_ok = output.get("duplicate_target") == expected.get("duplicate_target")
        fix_ok = output.get("fix_incomplete_candidate") == expected.get("fix_incomplete_candidate")
        relation_ok = output.get("lineage_relation") == expected.get("lineage_relation")
        exact = [action_ok, duplicate_ok, fix_ok, relation_ok]

        pre_fix_duplicate = 1.0
        if expected.get("lineage_relation") == "pre_fix_build":
            pre_fix_duplicate = 1.0 if action_ok and duplicate_ok else 0.0

        post_fix_no_duplicate = 1.0
        if str(expected.get("lineage_relation", "")).startswith("post_fix"):
            post_fix_no_duplicate = 1.0 if output.get("action") != "duplicate" else 0.0

        fix_incomplete_precision = 1.0
        if output.get("action") == "fix_incomplete" or expected.get("action") == "fix_incomplete":
            fix_incomplete_precision = 1.0 if action_ok and fix_ok else 0.0

        new_bug_precision = 1.0
        if output.get("action") == "new_bug" or expected.get("action") == "new_bug":
            new_bug_precision = 1.0 if action_ok else 0.0

        modal_collapse_resistance = 1.0
        expected_target = expected.get("duplicate_target")
        produced_target = output.get("duplicate_target")
        if produced_target in MODAL_MAGNETS and expected_target != produced_target:
            modal_collapse_resistance = 0.0

        evidence_text = " ".join(str(item) for item in (output.get("evidence") or []))
        evidence_quality = 1.0
        for required in expected.get("required_evidence", []):
            if required not in evidence_text:
                evidence_quality = 0.0
                break

        return {
            "accuracy": sum(1.0 for ok in exact if ok) / len(exact),
            "pre_fix_duplicate": pre_fix_duplicate,
            "post_fix_no_duplicate": post_fix_no_duplicate,
            "fix_incomplete_precision": fix_incomplete_precision,
            "new_bug_precision": new_bug_precision,
            "modal_collapse_resistance": modal_collapse_resistance,
            "evidence_quality": evidence_quality,
            "context_cost": float(output.get("context_tokens", len(str(output).split()))),
        }
