"""Temporal split API for OppieBugTriageBenchmark contamination control.

Replaces the random shuffle in ofs124960_synthetic_dataset.py with a
SWE-Bench-Pro-style temporal cutoff so models cannot have memorised future
tickets. Three tiers:

    train      → all candidate resolutiondates < TEMPORAL_TRAIN_CUTOFF
    test       → at least one candidate resolutiondate >= TEMPORAL_TEST_CUTOFF
                 (and none before TEMPORAL_TRAIN_CUTOFF for safety)
    held_out   → rows generated by harvest.py AFTER TEMPORAL_HELD_OUT_CUTOFF;
                 populated by P5 over time, empty at v1 snapshot

API only (per plan doc P6): the data regen is deferred until P5 has
≥3 weeks of harvested rows; running this at the current single-point-in-time
snapshot would be theatre. The split() function is callable today against any
list of rows from `search_set.jsonl` + `test_set.jsonl`, and reports
contamination metrics so callers can verify a candidate split obeys the
temporal cutoff.
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

DATA_DIR = Path(__file__).resolve().parent / "data"

# Cutoffs are deliberately wide; tighten once harvest history is denser.
TEMPORAL_TRAIN_CUTOFF = datetime(2026, 4, 1, tzinfo=timezone.utc)
TEMPORAL_TEST_CUTOFF = TEMPORAL_TRAIN_CUTOFF  # same boundary at v1 snapshot
TEMPORAL_HELD_OUT_CUTOFF = datetime(2026, 5, 1, tzinfo=timezone.utc)


def _parse_resolution_date(value: Any) -> datetime | None:
    if not value or not isinstance(value, str):
        return None
    # Production strings look like "2026-02-17T22:42:55.743-0800".
    try:
        return datetime.fromisoformat(value).astimezone(timezone.utc)
    except ValueError:
        return None


def _row_dates(row: dict[str, Any]) -> list[datetime]:
    out: list[datetime] = []
    for c in row.get("input", {}).get("candidates", []) or []:
        d = _parse_resolution_date(c.get("resolutiondate"))
        if d:
            out.append(d)
    return out


def split(
    rows: list[dict[str, Any]],
    train_cutoff: datetime = TEMPORAL_TRAIN_CUTOFF,
    test_cutoff: datetime = TEMPORAL_TEST_CUTOFF,
    held_out_cutoff: datetime = TEMPORAL_HELD_OUT_CUTOFF,
) -> dict[str, list[dict[str, Any]]]:
    """Split rows into train/test/held_out by candidate resolutiondate.

    train: every candidate's resolutiondate < train_cutoff
    test:  at least one candidate >= test_cutoff AND none < train_cutoff
    held_out: rows whose latest candidate resolutiondate >= held_out_cutoff
    overflow: rows with no resolutiondate (kept separate; never trained on)
    """
    train: list[dict[str, Any]] = []
    test: list[dict[str, Any]] = []
    held_out: list[dict[str, Any]] = []
    overflow: list[dict[str, Any]] = []
    for r in rows:
        dates = _row_dates(r)
        if not dates:
            overflow.append(r)
            continue
        max_d = max(dates)
        min_d = min(dates)
        if max_d >= held_out_cutoff:
            held_out.append(r)
        elif min_d >= test_cutoff:
            test.append(r)
        elif max_d < train_cutoff:
            train.append(r)
        else:
            # Straddles the boundary — treat as held-back to prevent leakage.
            overflow.append(r)
    return {"train": train, "test": test, "held_out": held_out, "overflow": overflow}


def contamination_report(splits: dict[str, list[dict[str, Any]]]) -> dict[str, Any]:
    """Return summary stats useful for verifying the split is leak-free."""

    def date_range(rows: list[dict[str, Any]]) -> dict[str, Any]:
        all_dates: list[datetime] = []
        for r in rows:
            all_dates.extend(_row_dates(r))
        if not all_dates:
            return {"n_rows": len(rows), "n_dates": 0}
        return {
            "n_rows": len(rows),
            "n_dates": len(all_dates),
            "min_date": min(all_dates).isoformat(),
            "max_date": max(all_dates).isoformat(),
        }

    report: dict[str, Any] = {tier: date_range(rows) for tier, rows in splits.items()}
    # Hard contamination check: any train row carrying a post-test-cutoff date.
    leaks = 0
    for r in splits["train"]:
        if any(d >= TEMPORAL_TEST_CUTOFF for d in _row_dates(r)):
            leaks += 1
    report["train_contamination_count"] = leaks
    report["contract"] = {
        "train_cutoff": TEMPORAL_TRAIN_CUTOFF.isoformat(),
        "test_cutoff": TEMPORAL_TEST_CUTOFF.isoformat(),
        "held_out_cutoff": TEMPORAL_HELD_OUT_CUTOFF.isoformat(),
    }
    return report


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--rows",
        type=Path,
        nargs="+",
        default=[DATA_DIR / "search_set.jsonl", DATA_DIR / "test_set.jsonl"],
        help="JSONL files to split (default: search_set.jsonl + test_set.jsonl)",
    )
    parser.add_argument(
        "--report-only",
        action="store_true",
        help="Print contamination report and exit; do NOT write split JSONL files. "
        "Default behaviour (no flag) is identical: P6 is API-only at v1 snapshot.",
    )
    args = parser.parse_args()
    rows: list[dict[str, Any]] = []
    for p in args.rows:
        if not p.exists():
            continue
        with p.open(encoding="utf-8") as fh:
            for line in fh:
                line = line.strip()
                if not line:
                    continue
                rows.append(json.loads(line))
    splits = split(rows)
    print(
        json.dumps(
            {
                "input_files": [str(p) for p in args.rows],
                "input_rows": len(rows),
                "split_sizes": {k: len(v) for k, v in splits.items()},
                "contamination_report": contamination_report(splits),
                "note": (
                    "P6 is API-only at v1 snapshot. Data regeneration deferred "
                    "until P5 has ≥3 weeks of harvested rows."
                ),
            },
            indent=2,
        )
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
